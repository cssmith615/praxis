class TestVerbDispatch:
    def test_verb_name_in_output(self):
        assert '"LOG"' in _gen("LOG.test")

    def test_target_in_output(self):
        assert '"test"' in _gen("LOG.test")

    def test_runtime_dispatch_called(self):
        assert "runtime.dispatch" in _gen("LOG.test")

    def test_await_present(self):
        assert "await" in _gen("LOG.test")

    def test_results_push_present(self):
        assert "results.push" in _gen("LOG.test")

    def test_multiple_steps_generate_multiple_dispatches(self):
        ts = _gen("LOG.a -> ANNOTATE.b -> OUT.c")
        assert ts.count("runtime.dispatch") == 3

    def test_set_verb_assigns_to_ctx_variables(self):
        steps = [
            VerbAction(verb="LOG", target=["x"], params={}),
            VerbAction(verb="SET", target=["myvar"], params={}),
        ]
        ts = TypeScriptGenerator().generate(_prog(*steps))
        assert "ctx.variables" in ts
        assert '"myvar"' in ts

    def test_params_serialized(self):
        ts = _gen('LOG.test(level=info)')
        assert "level" in ts or "info" in ts


