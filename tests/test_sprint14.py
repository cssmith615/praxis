class TestIfStatement:
    def test_if_generates_ts_if(self):
        cond = NamedCond(name="my_flag")
        then = VerbAction(verb="LOG", target=["x"], params={})
        stmt = IfStmt(condition=cond, then_body=then, else_body=None)
        ts = TypeScriptGenerator().generate(Program(statements=[stmt]))
        assert "if (" in ts

    def test_else_body_generated(self):
        cond = NamedCond(name="flag")
        then = VerbAction(verb="LOG", target=["x"], params={})
        else_ = VerbAction(verb="ANNOTATE", target=["y"], params={})
        stmt = IfStmt(condition=cond, then_body=then, else_body=else_)
        ts = TypeScriptGenerator().generate(Program(statements=[stmt]))
        assert "} else {" in ts

    def test_comparison_operator_mapped(self):
        cond = Comparison(left=VarRef(name="x"), op="==", right=5)
        then = VerbAction(verb="LOG", target=["y"], params={})
        stmt = IfStmt(condition=cond, then_body=then, else_body=None)
        ts = TypeScriptGenerator().generate(Program(statements=[stmt]))
        assert "===" in ts  # Praxis == → TypeScript ===


