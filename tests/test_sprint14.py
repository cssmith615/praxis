# ─── helpers ──────────────────────────────────────────────────────────────────

def _gen(text: str, **kwargs) -> str:
    gen = TypeScriptGenerator(**kwargs)
    return gen.generate(parse(text))


def _prog(*steps) -> Program:
    return Program(statements=[Chain(steps=list(steps))])


