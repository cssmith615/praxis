class TestCompileCLI:
    def test_compile_outputs_typescript(self):
        runner = CliRunner()
        result = runner.invoke(main, ["compile", "LOG.test"])
        assert result.exit_code == 0
        assert "async function run" in result.output

    def test_compile_target_typescript_explicit(self):
        runner = CliRunner()
        result = runner.invoke(main, ["compile", "--target", "typescript", "LOG.test"])
        assert result.exit_code == 0

    def test_compile_to_file(self):
        runner = CliRunner()
        with tempfile.TemporaryDirectory() as tmpdir:
            outfile = str(Path(tmpdir) / "output.ts")
            result = runner.invoke(main, ["compile", "LOG.test", "--out", outfile])
            assert result.exit_code == 0
            assert Path(outfile).exists()
            assert "async function run" in Path(outfile).read_text()

    def test_compile_to_file_writes_runtime_stub(self):
        runner = CliRunner()
        with tempfile.TemporaryDirectory() as tmpdir:
            outfile = str(Path(tmpdir) / "output.ts")
            result = runner.invoke(main, ["compile", "LOG.test", "--out", outfile])
            assert result.exit_code == 0
            runtime_file = Path(tmpdir) / "praxis-runtime.ts"
            assert runtime_file.exists()

    def test_compile_embed_runtime(self):
        runner = CliRunner()
        result = runner.invoke(main, ["compile", "--embed-runtime", "LOG.test"])
        assert result.exit_code == 0
        assert "PraxisRuntime" in result.output
        assert "import {" not in result.output

    def test_compile_invalid_program_exits_nonzero(self):
        runner = CliRunner()
        result = runner.invoke(main, ["compile", "NOT VALID !!!"])
        assert result.exit_code != 0

    def test_compile_from_file(self):
        runner = CliRunner()
        with tempfile.TemporaryDirectory() as tmpdir:
            px_file = str(Path(tmpdir) / "prog.px")
            Path(px_file).write_text("LOG.test -> ANNOTATE.x")
            result = runner.invoke(main, ["compile", "--file", px_file])
            assert result.exit_code == 0
            assert "LOG" in result.output
            assert "ANNOTATE" in result.output

    def test_compile_chain_generates_multiple_dispatches(self):
        runner = CliRunner()
        result = runner.invoke(main, ["compile", "LOG.a -> ANNOTATE.b -> OUT.c"])
        assert result.exit_code == 0
        assert result.output.count("runtime.dispatch") == 3
